package week_2_labs;

public class Switch_Case {

	public static void main(String[] args) {
		int tech = 1;
		switch(tech){
		case 1:
		{
		System.out.println("java");
		break;
		}
		case 2:{
		System.out.println("ES6");
		break;
		}
		default:{
		System.out.println("Not Listed..");
		}
		}
	}
}